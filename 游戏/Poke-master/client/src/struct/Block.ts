class BlockBasic {
    private x: Number;
    private y: Number;
    private idx: Number;
    private color: Number;
    private shapes: any;

    public constructor(x: Number, y: Number, idx: Number, color: Number, shapes: any) {
        this.x = x;
        this.y = y;
        this.idx = idx;
        this.color = color;
        this.shapes = shapes;
    };

}